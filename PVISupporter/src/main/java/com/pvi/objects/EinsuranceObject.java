package com.pvi.objects;

public class EinsuranceObject {
    private String id;
    private String seri;
    private String bks;
    private String lhbh;
    private String date;
    private String phone;
    private String feeStatus;

    public String getid() {
        return id;
    }

    public void setid(String id) {
        this.id = id;
    }

    public String getseri() {
        return seri;
    }

    public void setseri(String seri) {
        this.seri = seri;
    }

    public String getbks() {
        return bks;
    }

    public void setbks(String bks) {
        this.bks = bks;
    }

    public String getlhbh() {
        return lhbh;
    }

    public void setlhbh(String lhbh) {
        this.lhbh = lhbh;
    }

    public String getdate() {
        return date;
    }

    public void setdate(String date) {
        this.date = date;
    }

    public String getphone() {
        return phone;
    }

    public void setphone(String phone) {
        this.phone = phone;
    }

    public String getFeeStatus() {
        return feeStatus;
    }

    public void setFeeStatus(String feeStatus) {
        this.feeStatus = feeStatus;
    }


}
